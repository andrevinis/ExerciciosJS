// Escreva um programa que solicita ao usuário um número de 1 a 7 representando um dia da
// semana e exibe o nome correspondente a esse dia. Considere que 1 representa segunda-feira,
// 2 representa terça-feira e assim por diante.



// Escreva um programa que solicita ao usuário um número de 1 a 7

let dia = prompt ("Dias da semana")

if (dia == 1) {
    alert ("Segunda-feira")
}

else if (dia == 2) {
    alert ("Terça-feira")
}

else if (dia == 3) {
    alert ("Quarta-feira")
}

else if (dia == 4) {
    alert ("Quinta-feira")
}

else if (dia == 5) {
    alert ("Sexta-feira")
}

else if (dia == 6) {
    alert ("Sábado")
}

else if (dia == 7) {
    alert ("Domingo")
}